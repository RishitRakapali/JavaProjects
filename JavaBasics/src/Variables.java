public class Variables {
    public static void main(String[] args) {
        int myNumber = 69;
        System.out.println(myNumber);

        float myFloat = 315.68f;
        System.out.println(myFloat);

        // Double var holds large and small numbers
        // Also stores float values
        double myDouble = 14423;
        double yourDouble = 14.423;
        System.out.println(myDouble);
        System.out.println(yourDouble);

        // String encased in double quotes
        String myString = "Rish";
        System.out.println(myString);

        // Char is single letter encased in single quotes
        char myChar = 'a';
        System.out.println(myChar);

        boolean myBoolean = true;
        System.out.println(myBoolean);
    }
}
